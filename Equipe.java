
    public class Equipe {
        private String nome;
        private String mascote;
    
        public Equipe(String nome, String mascote) {
            this.nome = nome;
            this.mascote = mascote;
        }
    
        public String getNome() {
            return nome;
        }
    
        public String getMascote() {
            return mascote;
        }
    }